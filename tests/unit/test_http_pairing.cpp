TEST(PairingTest, OutOfOrderCalls) {
  boost::property_tree::ptree tree;

  setup(PRIVATE_KEY, PUBLIC_CERT);

  pair_session_t sess {};

  clientchallenge(sess, tree, "test");
  ASSERT_FALSE(tree.get<int>("root.paired") == 1);

  serverchallengeresp(sess, tree, "test");
  ASSERT_FALSE(tree.get<int>("root.paired") == 1);

  clientpairingsecret(sess, tree, "test");
  ASSERT_FALSE(tree.get<int>("root.paired") == 1);

  // This should work, it's the first time we call it
  sess.async_insert_pin.salt = "ff5dc6eda99339a8a0793e216c4257c4";
  getservercert(sess, tree, "test");
  ASSERT_TRUE(tree.get<int>("root.paired") == 1);

  // Calling it again should fail
  getservercert(sess, tree, "test");
  ASSERT_FALSE(tree.get<int>("root.paired") == 1);
}

TEST(CertChainTest, ConcurrentVerifyUsesIndependentOpenSslContexts) {
  crypto::cert_chain_t chain;
  auto named_cert = std::make_shared<crypto::named_cert_t>();
  named_cert->cert = PUBLIC_CERT;
  named_cert->name = "test";
  chain.add(named_cert);

  constexpr auto thread_count = 8;
  constexpr auto iterations = 200;
  std::atomic<bool> start {false};
  std::atomic<int> failures {0};
  std::vector<std::thread> threads;

  for (auto i = 0; i < thread_count; ++i) {
    threads.emplace_back([&]() {
      while (!start.load(std::memory_order_acquire)) {
        std::this_thread::yield();
      }

      for (auto iteration = 0; iteration < iterations; ++iteration) {
        auto cert = crypto::x509(PUBLIC_CERT);
        crypto::p_named_cert_t verified;
        auto err = chain.verify(cert.get(), verified);

        if (err != nullptr || verified.get() != named_cert.get()) {
          failures.fetch_add(1, std::memory_order_relaxed);
        }
      }
    });
  }

  start.store(true, std::memory_order_release);
  for (auto &thread : threads) {
    thread.join();
  }

  EXPECT_EQ(failures.load(std::memory_order_relaxed), 0);
}

TEST(CertChainTest, CanonicallyEquivalentCertificateReplacesExistingIdentity) {
  crypto::cert_chain_t chain;
  auto original = std::make_shared<crypto::named_cert_t>();
  original->cert = PUBLIC_CERT;
  original->name = "original";
  chain.add(original);

  auto replacement = std::make_shared<crypto::named_cert_t>();
  replacement->cert = with_crlf_line_endings(PUBLIC_CERT);
  replacement->name = "replacement";
  chain.add(replacement);

  auto cert = crypto::x509(PUBLIC_CERT);
  crypto::p_named_cert_t verified;
  ASSERT_EQ(chain.verify(cert.get(), verified), nullptr);
  EXPECT_EQ(verified.get(), replacement.get());
}

