std::string with_crlf_line_endings(std::string_view pem) {
  std::string converted;
  converted.reserve(pem.size() + std::count(pem.begin(), pem.end(), '\n'));
  for (const auto ch : pem) {
    if (ch == '\n') {
      converted.push_back('\r');
    }
    converted.push_back(ch);
  }
  return converted;
}

